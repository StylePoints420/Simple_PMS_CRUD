    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
        GetandSet_Customers_info var = new GetandSet_Customers_info();
        Parking_Functions func = new Parking_Functions();
        int fee = 100;
        String msg = "You do not have enough money. The parking fee is P100.";
        String carB = CBrandField.getText();
        String plate_num = Plate_NumField.getText();
        String entry = EntryField.getText();
        String exit = ExitField.getText();
        String date = Date_Field.getText();
        int cash = Integer.parseInt(PaymentField.getText());
       
        
        if(cash >= fee) {
            try {
                cash -= fee;
                var.setCar_Brand(carB);
                var.setPlate_Number(plate_num);
                var.setTime_IN(entry);
                var.setTime_OUT(exit);
                var.setCash(cash);
                var.setDate(date);
                 Connection db_connect = ConnectDB.Connect_to_DB();
            String sql_command = "SELECT * FROM drivers_info WHERE plate_number = '" + var.getPlate_Number() + "' ";
            PreparedStatement PS = db_connect.prepareStatement(sql_command);
            ResultSet rs = PS.executeQuery();
            if(rs.next()) {
                 JOptionPane.showMessageDialog(null, "Error! Duplicate entry for plate number! Try again!");    
              Plate_NumField.setText("");
               Plate_NumField.requestFocus();
            }
            else {
                func.Add(var);
                Read_tbl();
                CBrandField.setText("");
                Plate_NumField.setText("");
                EntryField.setText("");
                ExitField.setText("");
                Date_Field.setText("");
                PaymentField.setText("");
                CBrandField.requestFocus();
            }
              
               
    
            } catch (Exception ex) {
                Logger.getLogger(Main_Form.class.getName()).log(Level.SEVERE, null, ex);
            }
        } 
        
        else {
            JOptionPane.showMessageDialog(null, msg);
        PaymentField.requestFocus();
        }
        
       
    }//GEN-LAST:event_addbtnActionPerformed

