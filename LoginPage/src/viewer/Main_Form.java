    public void Read_tbl() {
        Parking_Functions read = new Parking_Functions();
        List<GetandSet_Customers_info> list = read.list();
        DefaultTableModel DFT = (DefaultTableModel) drivers_tbl.getModel();
        DFT.setRowCount(0);
        for(GetandSet_Customers_info info: list) {
            int id = info.getDriver_id();
            String brand = info.getCar_Brand();
            String platenum = info.getPlate_Number();
            String entrance = info.getTime_IN();
            String departure = info.getTime_OUT();
            String date1 = info.getDate();
            int money = info.getCash();
            DFT.addRow(new Object[]{id, brand, platenum, entrance, departure, money, date1});
            
        }
        
    }
    
